package com.njx.Basic6;

import java.util.*;
/**
 * 题目：计算在 [1, 20232023] 范围内有多少个整数与 2023 互质
 * 
 * 解题思路：
 * 1. 互质的定义：两个数的最大公约数为 1
 * 2. 使用容斥原理：先找出与 2023 不互质的数，再用总数减去
 * 3. 质因数分解：2023 = 7 × 17
 * 4. 与 2023 不互质的数包括：
 *    - 7 的倍数
 *    - 17 的倍数
 *    - 7 和 17 的公倍数（被重复计算，需要扣除）
 * 
 * 容斥原理公式：
 * |A ∪ B| = |A| + |B| - |A ∩ B|
 * 其中 A 是 7 的倍数集合，B 是 17 的倍数集合
 * 
 * 最终答案 = 总数 - |A ∪ B| = 总数 - |A| - |B| + |A ∩ B|
 * 
 * 数学知识点：
 * - 乘法快速幂：用于快速计算 a^p % mod
 * - 费马小定理求逆元：a^(p-2) ≡ a^(-1) (mod p)，当 p 为质数时
 * - 除法取模：(a/b) % mod = (a % mod) × b^(-1) % mod
 */
public class Test2 {
    static int mod = 1000000007; // 模数，一个大质数

    public static void main(String[] args) {
        // 计算范围上限：2023^2023
        long sum = power(2023, 2023);
        
        // 计算 7 的倍数的个数：sum / 7
        // 使用费马小定理求 7 的逆元：7^(mod-2) % mod
        long p1 = ((sum % mod) * power(7, mod - 2)) % mod;
        
        // 计算 17 的倍数的个数：sum / 17
        // 使用费马小定理求 17 的逆元：17^(mod-2) % mod
        long p2 = ((sum % mod) * power(17, mod - 2)) % mod;
        
        // 计算 7 和 17 公倍数的个数：sum / (7*17)
        // 这既是 7 的倍数也是 17 的倍数，在 p1 和 p2 中被重复计算了一次
        long p3 = ((sum % mod) * power(7*17, mod - 2)) % mod;

        // 根据容斥原理计算与 2023 互质的数的个数
        // 公式：ans = sum - p1 - p2 + p3
        // 加两次 mod 是为了防止减法产生负数（Java 中负数取模会得到负数）
        long ans = (sum - p1 - p2 + p3 + mod + mod) % mod;
        System.out.println(ans);
    }

    /**
     * 乘法快速幂算法：计算 (a^p) % mod
     * 时间复杂度：O(log p)
     * 
     * 算法原理：
     * 将指数 p 用二进制表示，例如 p = 13 = 1101(二进制)
     * 则 a^13 = a^8 × a^4 × a^1
     * 通过不断平方 a，并根据 p 的二进制位决定是否累乘到结果中
     * 
     * @param a 底数
     * @param p 指数
     * @return (a^p) % mod
     */
    static long power(long a, long p) {
        long ans = 1;
        a %= mod; // 先对底数取模，防止溢出

        while (p != 0) {
            // 如果当前二进制位为 1，则累乘到结果中
            if ((p & 1) == 1) {
                ans = (ans * a) % mod;
            }
            // 底数平方，准备下一位
            a = (a * a) % mod;
            // 右移一位，处理下一个二进制位
            p >>= 1;
        }

        return ans;
    }
}
