import java.util.*;
//现在做题都是有思路而且正确，但是很难实现
public class Test13 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        long n = sc.nextLong();  // 输入要查找的第N个有效数字

        // pre0[i] 表示位数不超过i位的有效数字总数
        // 例如：pre0[2] = 2位数的有效数字个数 = 45
        // pre0[3] = 2位数个数 + 3位数个数 = 45 + 9*5^2 = 45 + 225 = 270
        long[] pre0 = new long[19];
        pre0[2] = 9 * 5;  // 2位数：第一位有9种选择(1-9)，第二位有5种选择(与第一位奇偶相反)，共45个

        for (int i = 3; i < 19; i++) {
            // i位数的有效数字个数 = 9 * 5^(i-1)
            // 加上之前所有位数的总数
            pre0[i] = (long)(Math.pow(5, i - 1) * 9) + pre0[i - 1];
        }

        // pre[i] 表示i位数的有效数字个数（按某种分组后的数量）
        // 实际上 pre[i] = 5^i
        // 用于后续的计数分组
        long[] pre = new long[19];
        pre[1] = 5;  // 1位数有5个奇数（1,3,5,7,9），用于计算

        for (int i = 2; i < 19; i++) {
            pre[i] = pre[i - 1] * 5;  // pre[i] = 5^i
        }

        // 确定第n个有效数字的位数w
        int w = 2;  // 从2位数开始
        while (pre0[w] < n) {  // 如果当前位数及以下的总数小于n，说明n在更高位数中
            w++;  // 增加位数
        }
        // 循环结束后，w就是第n个有效数字的位数

        // oth 表示在当前位数w中的第几个（从1开始计数）
        // 例如：n=50，pre0[2]=45，那么oth=50-45=5，表示在3位数中的第5个
        long oth = n - pre0[w - 1];

        int g = w - 1;  // g表示除了第一位之外，还需要确定的位数
        long ans = 1;   // ans表示当前正在确定的数字（第一位为1，后续位为0或1表示奇偶）

        // 逐位确定数字
        // 策略：根据oth和pre[g]的关系，决定当前位是跳过一个分组还是确定一个数字
        while (g > 0) {
            if (oth <= pre[g]) {
                // 情况1：oth在pre[g]范围内，说明当前位可以确定
                // 输出当前位的数字
                System.out.print(ans);
                g--;  // 剩余位数减1
                if (g == 0) {
                    break;  // 如果所有位都确定了，退出循环
                }
                // 下一位的奇偶性与当前位相反
                // ans=1表示奇数，ans=0表示偶数
                ans = (ans % 2 == 1) ? 0 : 1;
            } else {
                // 情况2：oth大于pre[g]，说明需要跳过一整组
                // 更新ans的值（跳过一个数字）
                if (g == w - 1) {
                    // 如果是第一位，每次增加1（因为第一位是1,2,3,4,5,6,7,8,9）
                    ans += 1;
                } else {
                    // 如果是后续位，每次增加2（因为奇偶性固定，数字每次+2）
                    ans += 2;
                }
                oth -= pre[g];  // 减去跳过的这一组的数量
            }
        }

        // 确定最后一位数字
        // oth现在表示在最后一位分组中的位置
        if (oth == 0) {
            // 如果oth=0，表示是最后一组的最后一个数字
            // ans%2==1表示奇数，输出8；ans%2==0表示偶数，输出9
            System.out.print((ans % 2 == 1) ? 8 : 9);
        } else {
            // 否则根据oth计算最后一位的数字
            // 奇数位：数字为 1,3,5,7,9 对应 oth*2-2 = 0,2,4,6,8 对应数字 1,3,5,7,9
            // 偶数位：数字为 0,2,4,6,8 对应 oth*2-1 = 1,3,5,7,9 对应数字 0,2,4,6,8
            System.out.print((ans % 2 == 1) ? (oth * 2 - 2) : (oth * 2 - 1));
        }

        sc.close();
    }
}
