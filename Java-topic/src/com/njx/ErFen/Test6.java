package com.njx.ErFen;

import java.util.*;

public class Test6 {
    public static void main(String[] args) {
        // 创建输入对象，用于读取输入
        Scanner sc = new Scanner(System.in);

        // 读取输入的两个整数
        // n：一个小时有多少分钟（分钟范围 0 ~ n-1）
        // k：第二个按钮每次增加的值
        int n = sc.nextInt();
        int k = sc.nextInt();

        // dist数组：记录从 0 调到每个分钟数需要的最少按键次数
        // dist[i] 表示：从 0 调到 i 需要多少次操作
        int[] dist = new int[n];

        // 初始化数组
        // 因为最短路问题中需要判断是否访问过
        // 所以先全部设为 -1，表示“没有访问过”
        Arrays.fill(dist, -1);

        // 创建队列，用于 BFS（广度优先搜索）
        // BFS会一层一层扩展节点
        Queue<Integer> queue = new LinkedList<>();

        // 起点是 0（从0分钟开始调）
        dist[0] = 0;      // 到0本身不需要操作
        queue.offer(0);   // 把0加入队列

        // 当队列不为空时持续搜索
        while (!queue.isEmpty()) {

            // 取出队头元素
            int cur = queue.poll();

            // -------- 操作1：按 +1 按钮 --------
            // 按一次 +1
            int next1 = (cur + 1) % n;

            // 如果这个状态没有访问过
            if (dist[next1] == -1) {

                // 更新最短距离
                // 当前距离 +1（因为按了一次按钮）
                dist[next1] = dist[cur] + 1;

                // 加入队列，后面继续扩展
                queue.offer(next1);
            }

            // -------- 操作2：按 +k 按钮 --------
            int next2 = (cur + k) % n;

            // 如果没有访问过
            if (dist[next2] == -1) {

                // 更新距离
                dist[next2] = dist[cur] + 1;

                // 加入队列
                queue.offer(next2);
            }
        }
    }
}
